// js has a built in object namely Math that have properties and methods which we can use in our programs
// we can use Math.PI and Math.E to get constant values
// we can use methods like
// Math.abs(num) gives positive number like mod operator
// Math.pow(a,b) gives a^b
// Math.floor(num) gives floor of num
// Math.ceil(num) gives ceil of num
// Math.random() generates a random number i range [0,1)

console.log(Math.PI);// 3.141592653589793
console.log(Math.E);// 2.718281828459045

// using methods of Math object

console.log(Math.abs(10.5));// 10.5
console.log(Math.abs(-10.5));// 10.5

console.log(Math.pow(2,3));// 8

console.log(Math.floor(5));// 5
console.log(Math.floor(5.5));// 5
console.log(Math.floor(-5.5));// -6

console.log(Math.ceil(5));// 5
console.log(Math.ceil(5.5));// 6
console.log(Math.ceil(-5.5));// -5

console.log(Math.random());// 0.3296498515067836 this value this time it can generate any number between 0 and 1 including 0 not 1

