// arithmatic operators 

let num1 = 1;
let num2 = 2;

let sum = num1 + num2; // "+" -> addition operator
let prod = num1 * num2;// "*" -> multiplication operator
let sub = num1 - num2; // "-" -> subtraction operator
let div = num1 / num2; // "/" -> division operator
let rem = num1 % num2; // "%" -> modulus(remainder) operator
let exp = num1 ** num2;// "**"-> exponentiation operator


console.log(sum);
console.log(prod);
console.log(sub);
console.log(div);
console.log(rem);
